    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $owner = Auth::user();
        $nid = $_GET['id'];
        $owner->unreadNotifications->where('id',$nid)->markAsRead();

        $user = User::findOrFail($id);
        return view('admin.user.show', ['user'=>$user]);
    }

